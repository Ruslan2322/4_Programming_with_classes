package Lesson_04_Simple_classes_and_obj.Ex_7;

public class Dot {
    private double x;
    private double y;
    Dot (double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
